var createInfo = function(info) {
  var title = document.createElement('h2');
  title.textContent = info['title'];

  var date = document.createElement('p');
  date.textContent = info['date'];

  var extract = document.createElement('p');
  extract.textContent = info['extract'];

  var link = document.createElement('a');
  link.className = "btn btn-default";
  link.href = info['link'];
  link.role = "button";
  link.textContent = "Aller Ã  l'article \u00bb";

  var linkP = document.createElement('p');
  linkP.appendChild(link);

  var infoCol = document.createElement('div');
  infoCol.className = 'col-md-4';
  infoCol.appendChild(title);
  infoCol.appendChild(date);
  infoCol.appendChild(extract);
  infoCol.appendChild(link);

  return infoCol;
};


